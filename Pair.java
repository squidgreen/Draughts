class Pair {
  private int x;
  private int y;

  public Pair() {
    setX(0);
    setY(0);
  }

  /*
   * name: Pair
   * description: Constructs a Pair object with the given parameters
   */
  public Pair(int x, int y) {
    setX(x);
    setY(y);
  }

  private setX(int x) {
    this.x = x;
  }

  private setY(int y) {
    this.y = y;
  }
}
